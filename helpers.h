#ifndef _HELPERS
#define _HELPERS

#include <stdio.h>
#include <stdlib.h>

#define TRY(assertion, call_description)	\
	do {									\
		if (assertion) {					\
			fprintf(stderr, "(%s, %d): ",	\
					__FILE__, __LINE__);	\
			perror(call_description);		\
			exit(EXIT_FAILURE);				\
		}									\
	} while(0)

#endif